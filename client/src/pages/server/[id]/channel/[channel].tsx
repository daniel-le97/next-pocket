interface IProgressBar {
  start(): void;
  finish(): void;
}
const ServerOne: NextPage = () => {
  const router = useRouter();
  const user = pb.authStore.model;
  const { id, channel } = router.query as { id: string; channel: string };

  useEffect(() => {
    if (!id || !channel) {
      return;
    }

    const fetchServerData = async () => {
      try {
        // await serversService.getById(id);
        // await channelsService.getChannelsByServerId(id);
        await serversService.getMembers(id);
      } catch (error) {
        Pop.error(error);
      }
    };
    fetchServerData();
  }, [id]);

  useEffect(() => {
    if (!id || !channel) {
      return;
    }
    let messageSubscribe: UnsubscribeFunc | null;

    (async () => {
      const progress = new ProgressBar({
        size: 2,
        color: "#4b60dd",
        className: "bar-of-progress",
        delay: 100,
      }) as unknown as IProgressBar;
      
      try {
        progress.start();
        await channelsService.joinChannel({
          memberId: AppState.user!.id,
          channelId: channel,
        });
        await messageService.getMessagesByChannelId(channel);
        progress.finish();
        messageSubscribe = await messageService.subscribe();
      } catch (error) {
        progress.finish();
        Pop.error(error);
      }
    })();

    return () => {
      messageSubscribe ? messageSubscribe() : null;
    };
  }, [channel]);

  // useEffect(() => {
  //   const membership = AppState.activeServer?.members.find(
  //     (member) => member.user === user?.id
  //   );

  //   if (membership) {
  // // console.log('Not a member');
  //  router.push('/')
  //   }
  // });

  return (
    <>
      <Head>
        <title>next-pocket</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className="flex min-h-screen flex-col items-center justify-center ">
        <div className="flex  h-screen w-full  ">
          <ChannelsBar />
          <div className=" message-container ">
            <TopNavigation />

            {/* <MessageScroll /> */}
            <InfiniteMessageScroll />
            <CreateMessage />
          </div>
          <ServerMembersBar />
        </div>
      </main>
    </>
  );
};

export default observer(withAuth(ServerOne));
